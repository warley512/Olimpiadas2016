<h2>História</h2>
<P>A prática de movimentos semelhantes aos realizados hoje na ginástica artística 
	conta com relatos no Egito Antigo. Mas os historiadores apontam a Grécia como
	o berço da ginástica. A busca pelo corpo perfeito para praticar esportes e 
	para aperfeiçoar o desempenho militar estão na gênese da modalidade.</P>

	<p>Durante a Idade Média, após o declínio do Império Romano, o culto ao corpo
		perdeu força e a ginástica viveu um período de ostracismo, ficando restrita 
		praticamente aos acrobatas. O resgate só se deu no início do século 19, 
		quando, em 1811, o alemão Friedrich Ludwig Christoph Jahn fundou a primeira
		escola para a prática de ginástica ao ar livre.</p>

		<P>Embora o objetivo de Jahn não fosse esportivo e, sim, preparar fisicamente
			os jovens alemães para enfrentar o exército de Napoleão Bonaparte, a idéia se 
			difundiu por outros países europeus, que passaram a adotar os exercícios de ginástica.
			Ludwig Jahn criou os aparelhos cavalo com alças, barras horizontais, 
			trave e barras paralelas, além das modalidades de saltos. Ele é considerado o 
			“pai da ginástica”, mesmo tendo sido perseguido e preso depois que a prática do
			esporte foi considerada perigosa e de alto teor revolucionário.</P>

			<p>Mas a semente plantada por Jahn já tinha crescido e dado frutos. Muitos
				ginastas alemães trataram de disseminar o esporte por outras nações, entre
				elas o Brasil. Depois de décadas de proibição, em 1881 foi fundada, em Liege
				, na Bélgica, a Federação Europeia de Ginástica (FEG). O esporte ganhou um novo
				status, arrebatando cada vez mais fãs fascinados pela força, precisão e destreza dos ginastas.</p>

