<P> O vôlei nasceu em 1895 – um ano antes da primeira edição das Olimpíadas –
 na Associação Cristã de Moços de Holyoke, no estado norte-americano de Massachussets.
  A ideia veio do pastor Lawrence Rider, que sugeriu ao professor William G. Morgan a 
  criação de um esporte movimentado, mas de menor intensidade do que o basquete, outra
   modalidade recente que ganhava fama rapidamente. </P>

   <p>nfluenciado pelo tênis, Morgan decidiu colocar uma rede entre as duas equipes, 
   	evitando assim o contato físico característico do basquete. Inicialmente, a rede
   	 foi colocada a uma altura de 1,98m. O jogo foi batizado pelo professor Morgan de
   	  “mintonette”, mas a movimentação e o estilo de se praticar rapidamente o transformaram
   	   em “volley” – “torrente”, em inglês –, palavra que descreve também o “voleio”, jogada típica do tênis.</p>

   	   <P>A expansão da modalidade foi algo impressionante. Apenas um ano depois de sua criação,
   	    nos Estados Unidos, já havia praticantes no outro lado do mundo, em países como o Japão. 
   	    A disseminação ocorreu de uma forma tão rápida e eficiente que hoje o esporte detém uma 
   	    marca grandiosa: a Federação Internacional de Vôlei (FIBV, em inglês) é a federação que 
   	    mais possui países filiados entre todos os esportes, com 220.</P>

   	    <p>A história da modalidade nas Olimpíadas tem um fato interessante e até incomum.
   	     Em Tóquio-1968, o vôlei entrou no programa olímpico simultaneamente no masculino e
   	      no feminino. Desde a estreia, quando a União Soviética, entre os homens, e o Japão,
   	       entre as mulheres, conquistaram as primeiras medalhas de ouro, a modalidade não deixou de ser olímpica.</p>

   	       <p>No Brasil o vôlei virou um modelo de sucesso a ser seguido por outros esportes.
   	        Emplacando uma geração vencedora atrás da outra, de Atenas-2004 a Londres-2012, o 
   	        Brasil não ficou de fora do pódio uma vez sequer. Nesse período, o país conquistou
   	         um ouro (2004) e duas pratas (2008 e 2012) no masculino, além de dois ouros (2008 e 2012) no feminino.</p>

